с помощью лучевой терапии (радиотерапии). В частности, ученые из отделения медицинской физики 
Онкологического центра Джека Ади в Канаде, проанализировав научную литературу, сообщили о способности 
низких доз рентгеновских лучей снижать смертность от пневмонии в среднем примерно на 20%. А проводимые ранее
 испытания на животных показали, что острая фаза пневмонии может быть сокращена вдвое. В США уже опробовали 
 эту методику на 10 пациентах с СOVID-19. Получившие экспериментальное лечение инфицированные выздоравливали 
 за три дня против 12 дней в контрольной группе. Время до выписки из стационара было сокращено до 12 дней, в 
