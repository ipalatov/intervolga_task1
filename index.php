// обрезка строки, добавление многоточия. Т.к. многоточие '...'(3 символа) входит в общую длину, 
// то в функции указываем параметром 180+3 = 183 симовола. 
$cut = mb_strimwidth($a, 0, 183, '...');

// разделение строки по разделителю в массив
$array = explode(' ', $cut);

// удаление из массива элементов от первого до двух последних
$arrayTxt = array_splice($array, 0, count($array) - 2);

// объединение элементов массива в строку
// текстовая часть
$bTxt = implode(' ', $arrayTxt);
// ссылочная часть (оставшиеся последние 2 слова)
$bRef = implode(' ', $array);

// искомая переменная
$b = $bTxt . " " . "<a href=${link}>${bRef}</a>";
echo $b;

